<a href="http://draapurimac.gob.pe/" target="_blank"><img src="DRA.png"> <h5> <strong> Dirección Regional de Agricultura </strong> </h5> </a>


<P ALIGN="justify"> La Dirección Regional Agraria de Apurímac, es un órgano  desconcentrado del Gobierno Regional de Apurímac, funciona bajo la dependencia técnica, administrativa y normativa de la Gerencia Regional de Desarrollo Económico; depende presupuestalmente de la Gerencia de Planeamiento Presupuesto y Acondicionamiento Territorial del Gobierno Regional de Apurímac y mantiene relaciones de coordinación con el Ministerio de Agricultura de acuerdo al Convenio Marco Intergubernamental y el Convenio de Gestión suscritos.

<P ALIGN="justify">El ámbito jurisdiccional de la Dirección Regional Agraria de Apurímac, comprende a las provincias de: Abancay, Antabamba, Aymaraes, Cotabambas y Grau, en toda su extensión. El ámbito de aplicación alcanza a todas las dependencias que integran la Dirección Regional Agraria del Gobierno Regional de Apurímac, su sede regional está ubicada en la ciudad de Abancay, Jr. Huancavelica N° 723.

<P ALIGN="justify">La Dirección Regional Agraria de Apurímac, tiene por finalidad promover la rentabilidad y competitividad de los productores agrarios organizados en cadenas productivas en el ámbito regional, para lograr una agricultura desarrollada en términos de sostenibilidad económica, social y ambiental.



